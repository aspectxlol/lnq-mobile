/// Centralized animation duration constants
/// Use these throughout the app for consistency
class AppAnimations {
  // Dialog animations
  static const dialogDuration = Duration(milliseconds: 300);

  // Fade-in animations for list items
  static const fadeInShort = Duration(milliseconds: 50);
  static const fadeInMedium = Duration(milliseconds: 100);
  static const fadeInLong = Duration(milliseconds: 150);
  static const fadeInXLong = Duration(milliseconds: 300);

  // Screen transitions
  static const screenTransitionDuration = Duration(milliseconds: 300);
  static const pageTransitionDuration = Duration(milliseconds: 400);

  // Button animations
  static const buttonAnimationDuration = Duration(milliseconds: 200);
}
